public class Doors {
    String color;
    Double width;
    Double heigth;

    public Doors(){
        this.color = "Brown";
        this.width = 2.4;
        this.heigth = 1.5;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Double getWidth() {
        return width;
    }

    public void setWidth(Double width) {
        this.width = width;
    }

    public Double getHeigth() {
        return heigth;
    }

    public void setHeigth(Double heigth) {
        this.heigth = heigth;
    }
}
